			<div class="col-md-12">
				<h2 class="wow bounceIn" data-wow-delay="0.3s" data-wow-offset="50">
				<span style="color:#ff0000">Warrior Pointe's</span> Frequently Asked Questions</h2>
			</div>
			<div class="col-md-12 col-sm-12 col-xs-12 wow fadeInLeft" data-wow-delay="0.6s" data-wow-offset="50">
				<p>Want to learn more about Warrior Pointe?  Have a question you need answered?  Our FAQ is the place to be.  If you need an answer
				that is not listed on our website or in this FAQ, please ask us thru the contact for on the main page so we can get you an answer.</p>
				<ul>
				<li><span style="color:#ff0000">What is the total number of members? </span>Our current nationwide 
				membership is over 1,200.</li>
				<li><span style="color:#ff0000">What are some of the common activities that Warrior Pointe participates 
				in? </span>Warrior Pointe primarily focuses on local member interaction 
				through chapter meetings to strengthen the bond of brotherhood and 
				to foster the camaraderie. We also try to help with volunteering 
				with other organizations in any events that may be happening in 
				our immediate area. </li>
				<li><span style="color:#ff0000">What type of services does Warrior Pointe provide for the Veteran 
				Community? </span>Warrior Pointe provides varying degrees of support to 
				the veterans. We provide a safe place for likeminded people to meet 
				and discuss their problems with friends who understand exactly how 
				the other is feeling. We also provide more serious peer support 
				with a qualified group of veteran volunteers and provide a wide 
				range of assistance through referral to community based services.
				</li>
				<li><span style="color:#ff0000">Why should I join? </span>There are many veteran organizations that 
				do specific things. Warrior Pointe is unique in that we are a “one 
				stop” because we provide multiple services to our veteran brother’s 
				and sister’s. </li>
				<li><span style="color:#ff0000">What are the costs involved?</span> There are no dues or fees. We all 
				paid our dues when we raised our right arm and swore to protect 
				the Constitution by all means necessary up to and including our 
				life. </li>
				<li><span style="color:#ff0000">How do I join?</span> Joining is easy. Simply go to www.warriorpointe.org 
				and submit an application for the appropriate state that you reside 
				in. It is safe and secure. You will also need to provide proof of 
				service. </li>
				<li><span style="color:#ff0000">What are the requirements?</span> The only requirement for membership 
				in Warrior Pointe is that you have served honorably in the U.S. 
				Armed Forces (Army, Navy, Air Force, Marines and Coast Guard or 
				their reserve components) and can show proof of that service.
				</li>
				<li><span style="color:#ff0000">What does Warrior Pointe do that other veteran organizations 
				do not? </span>Warrior Pointe allows all veterans to join. Whether you 
				served in peacetime or in combat; if you are male or female. It 
				does not matter what era you served in or if you are still on Active 
				Duty or in a Reserve status. We do not discriminate and all are 
				welcome to join our ranks. </li>
				<li><span style="color:#ff0000">How do members from the same state interact if they are hundreds 
				of miles apart? </span>Although Warrior Pointe’s main goal is to form chapters 
				and have face to face interaction there may be times when a member 
				falls outside of a chapter boundaries. If this occurs we encourage 
				the member to take initiative and do word of mouth recruiting from 
				veterans in his or her community. We also have the ability to communicate 
				through Skype and other video chat medium to ensure members are 
				connected to one another. </li>
				<li><span style="color:#ff0000">Why should I choose Warrior Pointe over other organizations 
				such as V.F.W. or IAVA?</span> Warrior Pointe caters to all of our nation’s 
				veterans and we do not require a monthly or yearly fee to be involved 
				with us. We are also not affiliated with the Veterans Administration. 
				We are a group founded by veterans for veterans. Most organizations 
				also require you to be conflict affiliated. Not Warrior Pointe. 
				If you wore the uniform of our nation’s military then YOU are Warrior 
				Pointe! </li>
				<li><span style="color:#ff0000">Is Warrior Pointe a non-profit organization?</span> Yes, Warrior Pointe 
				Inc. has filed as a non-profit in the states of Idaho and California, 
				and has received Federal non-profit (501(c)3) status with the IRS. 
				</li>
				<li><span style="color:#ff0000">Does the staff get paid? </span>At this time, no one gets paid. From 
				chapter commander all the way to the top to the President of the 
				organization. We are an all-volunteer force. </li>
				<li><span style="color:#ff0000">How does Warrior Pointe Inc. make money?</span> Warrior Pointe at this 
				time makes limited funds through the sale of t-shirts through various 
				companies who donate these funds to our organization.</li>
				<li><span style="color:#ff0000">Does Warrior Pointe Inc. accept donations at this time?</span> Yes, 
				but we do not actively ask for it.</li>
				</ul>
